test('extracts Realux v2 proof envelope from JPEG trailer', () => {
  const raw = utf8ToBytes(`jpeg-bytes\nREALUX_PROOF_V2:${bytesToBase64Url(utf8ToBytes(JSON.stringify(envelope)))}`);
  assert.deepEqual(extractProofEnvelope(raw), envelope);
  assert.equal(new TextDecoder().decode(stripKnownProofTrailers(raw)), 'jpeg-bytes');
});

test('embeds and extracts Realux v2 proof envelope from JPEG COM segment', () => {
  const embedded = embedProofEnvelope(minimalJpeg(), envelope);
  assert.notDeepEqual(embedded, minimalJpeg());
  assert.deepEqual(extractProofEnvelope(embedded), envelope);
  assert.deepEqual(stripKnownProofTrailers(embedded), minimalJpeg());
});

test('COM proof survives iOS-style post-EOI trailer removal', () => {
  const embedded = embedProofEnvelope(minimalJpeg(), envelope);
  const eoi = embedded.lastIndexOf(0xd9);
  const iosNormalized = embedded.slice(0, eoi + 1);
  assert.deepEqual(extractProofEnvelope(iosNormalized), envelope);
});
